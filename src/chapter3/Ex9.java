package chapter3;

public class Ex9 {

	public static void main(String[] args) {
		//조건문 - switch,break
			//switch
			int num = 3;
			
			switch(num) {
			case 1:
				System.out.println("1입니다.");
				break;
			case 2:
				System.out.println("2입니다.");
				break;
			case 3:
				System.out.println("3입니다.");
			case 4:
				System.out.println("4입니다.");
				break;
			}  // end switch
			
			System.out.println("끝");
			
			
			
			
			
			
			
			
			
			
			
	} // end main
			
}// end class
